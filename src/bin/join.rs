use std::io;

use clap::Parser;

#[derive(Parser)]
#[command(
    version,
    about = "Join chunks of a file generated by 'split' back into a single file"
)]
struct Args {
    /// Path to directory with chunks
    source: String,

    /// Path for reassembled file
    #[arg(short, long = "dest")]
    destination: String,
}

fn main() -> io::Result<()> {
    let Args {
        source,
        destination,
    } = Args::parse();
    splitter::join(&source, &destination)
}
